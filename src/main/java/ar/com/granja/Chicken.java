package ar.com.granja;

public class Chicken {

	private Integer eggs;
	private String name;
	private int idFarm;
	private Integer idChicken;

	public Integer getEggs() {
		return eggs;
	}

	public void setEggs(Integer eggs) {
		this.eggs = eggs;
	}

	public int getIdFarm() {
		return idFarm;
	}

	public void setIdFarm(int idFarm) {
		this.idFarm = idFarm;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getIdChicken() {
		return idChicken;
	}

	public void setIdChicken(Integer idChicken) {
		this.idChicken = idChicken;
	}

}
