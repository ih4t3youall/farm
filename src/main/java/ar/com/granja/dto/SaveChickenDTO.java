package ar.com.granja.dto;

public class SaveChickenDTO {

	
	private int idFarm;
	private String name;
	private int eggs;
	public int getIdFarm() {
		return idFarm;
	}
	public void setIdFarm(int idFarm) {
		this.idFarm = idFarm;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getEggs() {
		return eggs;
	}
	public void setEggs(int eggs) {
		this.eggs = eggs;
	}
	
	
	
}
